import java.util.Scanner

fun main() {
    val scanner = Scanner(System.`in`)
    val line1: String = scanner.nextLine()
    val line2: String = scanner.nextLine()
    val number1: Int = scanner.nextInt()
    val number2: Int = scanner.nextInt()

    println(line1)
    println(line2)
    println(number1)
    println(number2)
}